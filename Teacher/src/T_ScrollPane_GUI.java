    public void run() {
   
        receiveIDS();
        DT = new T_DataTransfer(this);
        new Thread(DT).start();
    }
    
    private void initialize()
    {
        try {
            ID = (String)tgGUI.input.readUTF();
            name = (String)tgGUI.input.readUTF();
            update(ID, name);
        } catch (IOException ex) {
            System.out.println("IOException : "+ex);
        }
    }
    
    private void receiveIDS()
    {
        try {
            int n, i=0;
            String s;
            
            //Receive No. of host connections
            s = tgGUI.input.readUTF();
            n = Integer.parseInt(s);
            
            if(n==0)
                initialize();
            
            while(i<n)
            {
                ID = (String)tgGUI.input.readUTF();
                name = (String)tgGUI.input.readUTF();
                update(ID, name);               
                i++;
            }
            
            
        } catch (IOException ex) {
            System.out.println("IOException : "+ex);
        }
    }
    
    protected void update(String msg, String name)
    {
        dlm.addElement(name);
        hostNames[n] = name;
        hostID[n] = msg;
        create(n);
        n++;
    }
    
    protected void create(int i)
    {
        chtGUI[i] = new T_Chat_GUI(this, i);
    }
    
    protected void updatePanel(int i)
    {
        tgGUI.jPanel2.removeAll();
        tgGUI.jPanel2.repaint();
        tgGUI.jPanel2.revalidate();
        
        tgGUI.jPanel2.add(chtGUI[i].chatPanel);
        tgGUI.jPanel2.repaint();
        tgGUI.jPanel2.revalidate();
    }

    protected int getIndex()
    {
        return(jList1.getSelectedIndex());
    }
    
